    /**
     * Load real claims for current patient from ClaimDirectory
     */
    private void loadClaims() {
        // Get SystemData
        business.SystemData systemData = business.SystemData.getInstance();

        // Get all claims for current patient
        java.util.List<model.Claim> patientClaims = systemData.getCurrentPatientClaims();

        // Get selected filter
        String selectedFilter = (String) statusFilterComboBox.getSelectedItem();

        // Get table model
        javax.swing.table.DefaultTableModel tableModel = (javax.swing.table.DefaultTableModel) claimsTable.getModel();

        // Clear existing rows
        tableModel.setRowCount(0);

        // Add each claim to table (with filtering)
        for (model.Claim claim : patientClaims) {
            // Apply filter
            if (selectedFilter != null && !selectedFilter.equals("All Claims")) {
                String claimStatus = claim.getClaimStatus().getDisplayName();
                if (!claimStatus.equals(selectedFilter)) {
                    continue; // Skip claims that don't match the filter
                }
            }

            tableModel.addRow(new Object[] {
                    claim.getClaimNumber(),
                    claim.getPolicyNumber(),
                    claim.getFormattedServiceDate(),
                    claim.getProviderName(),
                    claim.getFormattedClaimAmount(),
                    claim.getClaimStatus().getDisplayName(),
                    claim.getFormattedSubmittedDate(),
                    claim.getFormattedLastUpdatedDate()
            });
        }
    }

    /**
     * Update statistics labels with real data
     */
    private void updateStatistics() {
        // Get SystemData
        business.SystemData systemData = business.SystemData.getInstance();

        // Get real counts
        int totalClaims = systemData.getClaimDirectory().getClaimCountForPatient(systemData.getCurrentPatientId());
        int pendingClaims = systemData.getCurrentPatientPendingClaimCount();
        int approvedClaims = systemData.getCurrentPatientApprovedClaimCount();
        int deniedClaims = systemData.getCurrentPatientDeniedClaimCount();

        // Update labels
        totalClaimsLabel.setText("Total: " + totalClaims);
        pendingClaimsStatsLabel.setText("Pending: " + pendingClaims);
        approvedClaimsStatsLabel.setText("Approved: " + approvedClaims);
        deniedClaimsStatsLabel.setText("Denied: " + deniedClaims);
    }

