    private void createEnterpriseButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createEnterpriseButtonActionPerformed
        // TODO add your handling code here:
        // Get input values
        String enterpriseName = enterpriseNameTextField.getText().trim();
        String selectedType = (String) enterpriseTypeComboBox.getSelectedItem();
        
        // Validate input
        if (enterpriseName.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this,
                "Please enter an enterprise name!",
                "Validation Error",
                javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if (selectedType == null || selectedType.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this,
                "Please select an enterprise type!",
                "Validation Error",
                javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        // Get SystemData
        business.SystemData systemData = business.SystemData.getInstance();
        
        // Check if enterprise name already exists
        if (systemData.getEnterpriseDirectory().isEnterpriseNameExists(enterpriseName)) {
            javax.swing.JOptionPane.showMessageDialog(this,
                "An enterprise with the name '" + enterpriseName + "' already exists!",
                "Duplicate Name",
                javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        // Convert selected type string to EnterpriseType enum
        model.Enterprise.EnterpriseType enterpriseType = null;
        
        switch (selectedType) {
            case "Hospital":
                enterpriseType = model.Enterprise.EnterpriseType.HOSPITAL;
                break;
            case "Insurance Provider":
                enterpriseType = model.Enterprise.EnterpriseType.INSURANCE_PROVIDER;
                break;
            case "Pharmacy Chain":
                enterpriseType = model.Enterprise.EnterpriseType.PHARMACY_CHAIN;
                break;
            case "Pharmaceutical Supplier":
                enterpriseType = model.Enterprise.EnterpriseType.PHARMACEUTICAL_SUPPLIER;
                break;
            default:
                javax.swing.JOptionPane.showMessageDialog(this,
                    "Invalid enterprise type selected!",
                    "Error",
                    javax.swing.JOptionPane.ERROR_MESSAGE);
                return;
        }
        
        // Create the enterprise using SystemData
        model.Enterprise newEnterprise = systemData.createEnterprise(enterpriseName, enterpriseType);
        
        // Reload the table to show the new enterprise
        loadEnterprises();
        
        // Show success message
        javax.swing.JOptionPane.showMessageDialog(this,
            "Enterprise '" + enterpriseName + "' created successfully!\n" +
            "Enterprise ID: " + newEnterprise.getEnterpriseId(),
            "Success",
            javax.swing.JOptionPane.INFORMATION_MESSAGE);
        
        // Clear the form
        enterpriseNameTextField.setText("");
        enterpriseTypeComboBox.setSelectedIndex(0);
        enterpriseNameTextField.requestFocus();
    }//GEN-LAST:event_createEnterpriseButtonActionPerformed

