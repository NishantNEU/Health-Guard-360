    private void viewDocumentsButtonActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_viewDocumentsButtonActionPerformed
        // TODO add your handling code here:
        if (claimNumberValueLabel.getText().equals("Select a claim to view details")) {
            javax.swing.JOptionPane.showMessageDialog(this,
                    "Please select a claim from the table first!",
                    "No Claim Selected",
                    javax.swing.JOptionPane.WARNING_MESSAGE);
        } else {
            javax.swing.JOptionPane.showMessageDialog(this,
                    "Opening supporting documents for claim: " + claimNumberValueLabel.getText() +
                            "\n\nDocuments:\n- Hospital Bill.pdf\n- Doctor's Notes.pdf\n- Lab Results.pdf" +
                            "\n\n(Document viewer will be implemented later)",
                    "Claim Documents",
                    javax.swing.JOptionPane.INFORMATION_MESSAGE);
        }
    }// GEN-LAST:event_viewDocumentsButtonActionPerformed

    private void withdrawClaimButtonActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_withdrawClaimButtonActionPerformed
        // TODO add your handling code here:
        if (claimNumberValueLabel.getText().equals("Select a claim to view details")) {
            javax.swing.JOptionPane.showMessageDialog(this,
                    "Please select a claim from the table first!",
                    "No Claim Selected",
                    javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        String claimNumber = claimNumberValueLabel.getText();

        // Get SystemData
        business.SystemData systemData = business.SystemData.getInstance();
        model.Claim claim = systemData.getClaimDirectory().findClaimByNumber(claimNumber);

        if (claim == null) {
            return;
        }

        // Check if can be withdrawn
        if (!claim.canBeWithdrawn()) {
            javax.swing.JOptionPane.showMessageDialog(this,
                    "Cannot withdraw a claim with status: " + claim.getClaimStatus().getDisplayName() +
                            "\n\nOnly 'Submitted' or 'Under Review' claims can be withdrawn.",
                    "Cannot Withdraw",
                    javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Confirm withdrawal
        int response = javax.swing.JOptionPane.showConfirmDialog(this,
                "Are you sure you want to withdraw claim " + claimNumber + "?\n\n" +
                        "This action cannot be undone.",
                "Withdraw Claim",
                javax.swing.JOptionPane.YES_NO_OPTION,
                javax.swing.JOptionPane.WARNING_MESSAGE);

        if (response == javax.swing.JOptionPane.YES_OPTION) {
            // Withdraw using SystemData
            boolean success = systemData.withdrawClaim(claimNumber);

            if (success) {
                javax.swing.JOptionPane.showMessageDialog(this,
                        "Claim " + claimNumber + " has been withdrawn successfully.",
                        "Claim Withdrawn",
                        javax.swing.JOptionPane.INFORMATION_MESSAGE);

                // Reload table and stats
                loadClaims();
                updateStatistics();

                // Clear details
                claimNumberValueLabel.setText("Select a claim to view details");
                policyNumberValueLabel.setText("--");
                diagnosisValueLabel.setText("--");
                serviceTypeValueLabel.setText("--");
                amountValueLabel.setText("--");
                statusValueLabel.setText("--");
                submittedDateValueLabel.setText("--");
                lastUpdatedValueLabel.setText("--");
            }
        }
    }// GEN-LAST:event_withdrawClaimButtonActionPerformed

