    /**
     * Load all enterprises from EnterpriseDirectory into table
     */
    private void loadEnterprises() {
        // Get SystemData instance
        business.SystemData systemData = business.SystemData.getInstance();
        
        // Get all enterprises
        java.util.List<model.Enterprise> enterpriseList = systemData.getEnterpriseDirectory().getAllEnterprises();
        
        // Get table model
        javax.swing.table.DefaultTableModel tableModel = (javax.swing.table.DefaultTableModel) enterprisesTable.getModel();
        
        // Clear existing rows
        tableModel.setRowCount(0);
        
        // Add each enterprise to table
        for (model.Enterprise enterprise : enterpriseList) {
            tableModel.addRow(new Object[]{
                enterprise.getEnterpriseId(),
                enterprise.getEnterpriseName(),
                enterprise.getEnterpriseType().getDisplayName(),
                enterprise.getFormattedCreatedDate()
            });
        }
    }
