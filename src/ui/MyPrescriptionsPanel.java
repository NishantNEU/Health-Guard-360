    private void prescriptionsTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_prescriptionsTableMouseClicked
        // TODO add your handling code here:
        int selectedRow = prescriptionsTable.getSelectedRow();
        
        if (selectedRow >= 0) {
            String rxNumber = prescriptionsTable.getValueAt(selectedRow, 0).toString();
            
            business.SystemData systemData = business.SystemData.getInstance();
            model.Prescription rx = systemData.getPrescriptionDirectory().findPrescriptionByNumber(rxNumber);
            
            if (rx != null) {
                // Update detail labels
                rxNumberValueLabel.setText(rx.getPrescriptionNumber());
                medicationNameValueLabel.setText("Medication: " + rx.getDosage());
                dosageValueLabel.setText(rx.getDosage());
                doctorValueLabel.setText("Dr. Sarah Johnson, MD");
                refillsValueLabel.setText(rx.getRefillStatusMessage());
                pharmacyValueLabel.setText("CVS Pharmacy - Main St");
                coverageValueLabel.setText("80% covered (Tier 1)");
                copayValueLabel.setText("$10.00");
                
                // Color code refills
                if (rx.getRefillsRemaining() == 0) {
                    refillsValueLabel.setForeground(new java.awt.Color(220, 53, 69)); // Red
                } else if (rx.getRefillsRemaining() <= 1) {
                    refillsValueLabel.setForeground(new java.awt.Color(255, 152, 0)); // Orange
                } else {
                    refillsValueLabel.setForeground(new java.awt.Color(40, 167, 69)); // Green
                }
            }
        }
    }//GEN-LAST:event_prescriptionsTableMouseClicked

    private void requestRefillButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_requestRefillButtonActionPerformed
        // TODO add your handling code here:
         if (rxNumberValueLabel.getText().equals("Select a prescription to view details")) {
            javax.swing.JOptionPane.showMessageDialog(this,
                "Please select a prescription from the table first!",
                "No Prescription Selected",
                javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        String rxNumber = rxNumberValueLabel.getText();
        business.SystemData systemData = business.SystemData.getInstance();
        model.Prescription rx = systemData.getPrescriptionDirectory().findPrescriptionByNumber(rxNumber);
        
        if (rx == null) {
            return;
        }
        
        // Check if can refill
        if (!rx.canRefill()) {
            javax.swing.JOptionPane.showMessageDialog(this,
                "No refills remaining for this prescription.\n\n" +
                "Please contact your doctor to renew your prescription.",
                "No Refills Available",
                javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        // Confirm refill
        int response = javax.swing.JOptionPane.showConfirmDialog(this,
            "Request refill for:\n\n" +
            "Medication: " + rx.getDosage() + "\n" +
            "Pharmacy: " + pharmacyValueLabel.getText() + "\n" +
            "Patient Copay: " + copayValueLabel.getText() + "\n\n" +
            "Do you want to proceed?",
            "Request Refill",
            javax.swing.JOptionPane.YES_NO_OPTION,
            javax.swing.JOptionPane.QUESTION_MESSAGE);
        
        if (response == javax.swing.JOptionPane.YES_OPTION) {
            // Process refill
            boolean success = systemData.requestRefill(rxNumber);
            
            if (success) {
                javax.swing.JOptionPane.showMessageDialog(this,
                    "Refill request submitted successfully!\n\n" +
                    "Your prescription will be ready for pickup at:\n" +
                    pharmacyValueLabel.getText() + "\n\n" +
                    "Expected ready time: Today by 5:00 PM\n\n" +
                    "You will receive a text notification when ready.",
                    "Refill Requested",
                    javax.swing.JOptionPane.INFORMATION_MESSAGE);
                
                // Reload table and stats
                loadPrescriptions();
                updateStatistics();
                
                // Refresh selected row details
                int selectedRow = prescriptionsTable.getSelectedRow();
                if (selectedRow >= 0) {
                    prescriptionsTableMouseClicked(null);
                }
            }
        }
    }//GEN-LAST:event_requestRefillButtonActionPerformed

