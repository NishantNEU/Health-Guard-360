    private void claimsTableMouseClicked(java.awt.event.MouseEvent evt) {// GEN-FIRST:event_claimsTableMouseClicked
        // TODO add your handling code here:
        // Get selected row
        int selectedRow = claimsTable.getSelectedRow();

        if (selectedRow >= 0) {
            // Get claim number from selected row
            String claimNumber = claimsTable.getValueAt(selectedRow, 0).toString();

            // Get SystemData
            business.SystemData systemData = business.SystemData.getInstance();

            // Find the claim
            model.Claim selectedClaim = systemData.getClaimDirectory().findClaimByNumber(claimNumber);

            if (selectedClaim != null) {
                // Update all detail labels with real data
                claimNumberValueLabel.setText(selectedClaim.getClaimNumber());
                policyNumberValueLabel.setText(selectedClaim.getPolicyNumber());
                diagnosisValueLabel.setText(selectedClaim.getDiagnosis());
                serviceTypeValueLabel.setText(selectedClaim.getServiceType().getDisplayName());
                amountValueLabel.setText(selectedClaim.getFormattedClaimAmount());
                statusValueLabel.setText(selectedClaim.getClaimStatus().getDisplayName());
                submittedDateValueLabel.setText(selectedClaim.getFormattedSubmittedDate());
                lastUpdatedValueLabel.setText(selectedClaim.getFormattedLastUpdatedDate());

                // Color code status
                model.Claim.ClaimStatus status = selectedClaim.getClaimStatus();
                if (status == model.Claim.ClaimStatus.APPROVED || status == model.Claim.ClaimStatus.PAID) {
                    statusValueLabel.setForeground(new java.awt.Color(40, 167, 69)); // Green
                } else if (status == model.Claim.ClaimStatus.DENIED) {
                    statusValueLabel.setForeground(new java.awt.Color(220, 53, 69)); // Red
                } else if (status == model.Claim.ClaimStatus.UNDER_REVIEW) {
                    statusValueLabel.setForeground(new java.awt.Color(255, 152, 0)); // Orange
                } else {
                    statusValueLabel.setForeground(new java.awt.Color(33, 150, 243)); // Blue
                }
            }
        }
    }// GEN-LAST:event_claimsTableMouseClicked

