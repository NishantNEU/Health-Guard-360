    /**
     * Load real prescriptions for current patient
     */
    private void loadPrescriptions() {
        business.SystemData systemData = business.SystemData.getInstance();
        java.util.List<model.Prescription> patientRx = systemData.getCurrentPatientPrescriptions();
        
        javax.swing.table.DefaultTableModel tableModel = (javax.swing.table.DefaultTableModel) prescriptionsTable.getModel();
        tableModel.setRowCount(0);
        
        for (model.Prescription rx : patientRx) {
            tableModel.addRow(new Object[]{
                rx.getPrescriptionNumber(),
                "Medication Name", // Will be linked to Medication object later
                rx.getDosage(),
                rx.getQuantity() + " units",
                rx.getRefillsRemaining(),
                rx.getFormattedPrescribedDate(),
                "Pharmacy Name", // Will be linked to Pharmacy later
                rx.getStatus().getDisplayName()
            });
        }
    }
    
    /**
     * Update statistics
     */
    private void updateStatistics() {
        business.SystemData systemData = business.SystemData.getInstance();
        
        int active = systemData.getCurrentPatientActivePrescriptionCount();
        int readyForRefill = systemData.getCurrentPatientReadyForRefillCount();
        int total = systemData.getCurrentPatientTotalMedicationCount();
        
        activeRxLabel.setText("Active: " + active);
        readyForRefillLabel.setText("Ready for Refill: " + readyForRefill);
        totalMedicationsLabel.setText("Total Medications: " + total);
    }

