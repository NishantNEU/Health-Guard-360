    private void createOrganizationButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createOrganizationButtonActionPerformed
        // TODO add your handling code here:
        // Get input values
        String selectedEnterpriseName = (String) enterpriseComboBox.getSelectedItem();
        String organizationName = organizationNameTextField.getText().trim();
        String selectedOrgType = (String) organizationTypeComboBox.getSelectedItem();
        
        // Validate enterprise selection
        if (selectedEnterpriseName == null || selectedEnterpriseName.equals("-- Select Enterprise --")) {
            javax.swing.JOptionPane.showMessageDialog(this,
                "Please select an enterprise!",
                "Validation Error",
                javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        // Validate organization name
        if (organizationName.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this,
                "Please enter an organization name!",
                "Validation Error",
                javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        // Get SystemData
        business.SystemData systemData = business.SystemData.getInstance();
        
        // Find the selected enterprise
        model.Enterprise selectedEnterprise = systemData.getEnterpriseDirectory().findEnterpriseByName(selectedEnterpriseName);
        
        if (selectedEnterprise == null) {
            javax.swing.JOptionPane.showMessageDialog(this,
                "Selected enterprise not found!",
                "Error",
                javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        // Check if organization name already exists in this enterprise
        if (systemData.getOrganizationDirectory().isOrganizationNameExistsInEnterprise(organizationName, selectedEnterprise.getEnterpriseId())) {
            javax.swing.JOptionPane.showMessageDialog(this,
                "An organization with the name '" + organizationName + "' already exists in " + selectedEnterpriseName + "!",
                "Duplicate Name",
                javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        // Convert org type string to enum
        model.Organization.OrganizationType orgType = null;
        
        switch (selectedOrgType) {
            case "Department":
                orgType = model.Organization.OrganizationType.DEPARTMENT;
                break;
            case "Division":
                orgType = model.Organization.OrganizationType.DIVISION;
                break;
            case "Branch":
                orgType = model.Organization.OrganizationType.BRANCH;
                break;
            case "Unit":
                orgType = model.Organization.OrganizationType.UNIT;
                break;
            default:
                javax.swing.JOptionPane.showMessageDialog(this,
                    "Invalid organization type!",
                    "Error",
                    javax.swing.JOptionPane.ERROR_MESSAGE);
                return;
        }
        
        // Create the organization using SystemData (this also links it to the enterprise)
        model.Organization newOrganization = systemData.createOrganization(organizationName, orgType, selectedEnterprise.getEnterpriseId());
        
        // Reload the table
        loadOrganizations();
        
        // Show success message
        javax.swing.JOptionPane.showMessageDialog(this,
            "Organization '" + organizationName + "' created successfully under " + selectedEnterpriseName + "!\n" +
            "Organization ID: " + newOrganization.getOrganizationId(),
            "Success",
            javax.swing.JOptionPane.INFORMATION_MESSAGE);
        
        // Clear the form
        enterpriseComboBox.setSelectedIndex(0);
        organizationNameTextField.setText("");
        organizationTypeComboBox.setSelectedIndex(0);
        organizationNameTextField.requestFocus();
    }//GEN-LAST:event_createOrganizationButtonActionPerformed

