    /**
     * Load enterprises into dropdown from SystemData
     */
    private void loadEnterprises() {
        // Get SystemData
        business.SystemData systemData = business.SystemData.getInstance();
        
        // Clear existing items
        enterpriseComboBox.removeAllItems();
        
        // Add default option
        enterpriseComboBox.addItem("-- Select Enterprise --");
        
        // Get all enterprises and add to dropdown
        java.util.List<model.Enterprise> enterpriseList = systemData.getEnterpriseDirectory().getAllEnterprises();
        
        for (model.Enterprise enterprise : enterpriseList) {
            enterpriseComboBox.addItem(enterprise.getEnterpriseName());
        }
    }
    
    /**
     * Load all organizations from OrganizationDirectory into table
     */
    private void loadOrganizations() {
        // Get SystemData
        business.SystemData systemData = business.SystemData.getInstance();
        
        // Get all organizations
        java.util.List<model.Organization> organizationList = systemData.getOrganizationDirectory().getAllOrganizations();
        
        // Get table model
        javax.swing.table.DefaultTableModel tableModel = (javax.swing.table.DefaultTableModel) organizationsTable.getModel();
        
        // Clear existing rows
        tableModel.setRowCount(0);
        
        // Add each organization to table
        for (model.Organization organization : organizationList) {
            // Find enterprise name
            model.Enterprise enterprise = systemData.getEnterpriseDirectory().findEnterpriseById(organization.getEnterpriseId());
            String enterpriseName = (enterprise != null) ? enterprise.getEnterpriseName() : "Unknown";
            
            tableModel.addRow(new Object[]{
                organization.getOrganizationId(),
                organization.getOrganizationName(),
                organization.getOrganizationType().getDisplayName(),
                enterpriseName,
                organization.getFormattedCreatedDate()
            });
        }
    }

